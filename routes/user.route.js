const express = require("express");
const UserRoute = express.Router();
const userController = require("../controllers/user.controller");
const { verifyUser, verifyAdmin } = require("../middlewares/authenticate");

/**
 * @swagger
 * /users:
 *   get:
 *     summary: Lấy danh sách tất cả người dùng
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: Danh sách user
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 title:
 *                   type: string
 *                   example: User List
 *                 users:
 *                   type: array
 *                   items:
 *                     $ref: '#/components/schemas/User'
 *       401:
 *         description: Unauthorized | Not admin
 */
UserRoute.get("/", verifyUser, verifyAdmin, userController.getAllUsers);

/**
 * @swagger
 * /users/{id}:
 *   put:
 *     summary: Cập nhật role người dùng (isAdmin)
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *         description: ID của user
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               isAdmin:
 *                 type: boolean
 *                 example: true
 *     responses:
 *       200:
 *         description: Cập nhật thành công
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/User'
 *       404:
 *         description: Không tìm thấy user
 */
UserRoute.put("/:id", verifyUser, verifyAdmin, userController.updateUser);

module.exports = UserRoute;
